export function resolveRefUrl(ref: string): string | null {
  const t = ref.trim();
  if (REG_URLS[t]) return REG_URLS[t];
  const key = Object.keys(REG_URLS).find(k =>
    t.toLowerCase().startsWith(k.toLowerCase()) || k.toLowerCase().startsWith(t.toLowerCase()),
  );
  return key ? REG_URLS[key] : null;
}

export function splitRefsLine(content: string): { body: string; refsLine: string | null } {
  const lines = content.split("\n");
  const refsIdx = lines.reduce(
    (acc, line, i) => (/^refs?:/i.test(line.trim()) ? i : acc),
    -1,
  );
  if (refsIdx < 0) return { body: content, refsLine: null };
  return {
    body:    lines.slice(0, refsIdx).join("\n").trimEnd(),
    refsLine: lines[refsIdx],
  };
}
